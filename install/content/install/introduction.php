<h1>Welcome to installation</h1>
<p class="text-info">With this step by step guide it is possible to install the whole IME - Backend</p>
<p class="text-warning"><span class="label label-warning">Note</span> The Frontend needs to be installed and configured seperately</p>
<p>In order to proceed, you will need your database settings. If you do not know your database settings,
please contact your Server Administrator. You won't be able to continue without them. You need: </p>
<ul>
    <li>The Database Type - <span class="muted">the database you will be using.</span></li>
    <li>The Database server hostname or DSN - <span class="muted">the address of the database server.</span></li>
    <li>The Database server port - <span class="muted">the port of the database server (most of the time this is not needed).</span></li>
    <li>The Database name - <span class="muted">the name of the database on the server.</span></li>
    <li>The Database username and Database password - <span class="muted">the login data to access the database.</span></li>
</ul>
<p>Currently IME supports the following databases:</p>
<ul>
    <li>MySQL (MySQLi supported)</li>
</ul>
